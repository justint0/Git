const areaCircle = function(radius) {
  return Math.PI * (radius ** 2);
}

const areaTriangle = function(base, height) {
  return 0.5 * base * height;
}

const areaSquare = function(length) {
  return length ** 2;
}

const areaRectangle = function(length, width) {
  return length * width;
}

const areaParallelogram = function(base, verticalHeight) {
  return base * verticalHeight;
}